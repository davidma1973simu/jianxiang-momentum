// 求助过滤网结论：根据四个选项给出活人话建议
export function helpConclusion({ who, what, done, intent }) {
  if (done === '还没动' && intent === '想直接拿答案') {
    return '先别急着问。先自己搜一下/试一下，哪怕只是最小的一步。直接拿答案容易把主动权交出去。';
  }
  if (who === '专业人士' && what === '我要一个决定') {
    return '专业人士能给你信息和经验，但最后按不按 TA 说的做，决定权在你。先问信息，不急着要结论。';
  }
  if (what === '我只是想吐槽' && intent === '想直接拿答案') {
    return '你其实更想被听见。不妨直接告诉对方：「我现在不需要建议，只想让你听一下。」';
  }
  if (who === '懂我' && what === '我要情绪支持') {
    return '找懂你的人聊聊挺好，但先确认对方现在有余力。一句「我想占用你十分钟」就够了。';
  }
  if (intent === '想换角度看') {
    return '换角度看很好。可以问 TA：「如果是你，你会怎么看这件事？」别急着要标准答案。';
  }
  return '借力不是偷懒，是先看清自己要什么，再借别人的眼睛/经验。是信息、支持、还是替你决定？越清楚，越不容易白问。';
}
